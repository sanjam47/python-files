a="abcdefghijkl"

#prints from 0 index to 1(not inc)
print(a[0:1])
#prints from 0 index to last
print(a[0:])
#prints from 1 index to last
print(a[1:])
#prints values from 0 to 2(not inc)
print(a[:2])
#prints values from 0 to 3(not inc)
print(a[:3])
#prints value from 1 to 5(not inc)
print(a[1:5])
#prints value from 1 to 8 but skipping by 2 values.
print(a[1:8:2])
#prints value from start to end but from -1 or reversing the string.
print(a[::-1])